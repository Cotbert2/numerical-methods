%Una bola de 4k cuelga de una cuerda de masa despreciable del techo del
%ascensor de un ascensor. Calcular la tension de la cuerda en los
%siguientes casos (g = 10 m/s)

%Datos
gravity = 10
mass = 4;


%a) Cuando acelera hacia arriba a 3m/s^s

T =  mass * gravity + mass * 3

%b) cuando se mueve con velocidad constante
T = mass * gravity

%c) Cuando frena dismunitendo su velocidad a 3m/s^2

T =  mass * gravity - mass * 3


%d) Cuando se rompe el cable que sostiene el asensor libremente
T = 0 